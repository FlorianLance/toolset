#pragma once

// local
#include "geometry/point3.hpp"

namespace tool::geo {

template<typename acc>
struct Plane3;

using Plane3f = Plane3<float>;
using Plane3d = Plane3<double>;

template<typename acc>
struct Plane3{
    Vec3<acc> normal = {1,0,0};
    acc distance{0};
};

template<typename acc>
constexpr auto plane_equation(const Pt3<acc> &pt, const Plane3<acc> &plane) -> acc{
    return dot(pt, plane.normal) - plane.distance;
}
}
