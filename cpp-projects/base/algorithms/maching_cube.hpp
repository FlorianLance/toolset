template <typename T>
struct TRIANGLE
{
   geo::Point3<T> p[3];
};

template <typename T1, typename T2>
struct GRIDCELL
{
   geo::Point3<T1> p[8];
   T2 val[8];
};

/*
   Linearly interpolate the position where an isosurface cuts
   an edge between two vertices, each with their own scalar value
*/
template <typename T>
geo::Point3<T> VertexInterp(T isolevel, geo::Point3<T> p1, geo::Point3<T> p2,T valp1,T valp2){

    T mu;
    geo::Point3<T> p;

    if (std::abs(isolevel-valp1) < 0.00001)
        return(p1);
    if (std::abs(isolevel-valp2) < 0.00001)
        return(p2);
    if (std::abs(valp1-valp2) < 0.00001)
        return(p1);
    mu = (isolevel - valp1) / (valp2 - valp1);
    p.x() = p1.x() + mu * (p2.x() - p1.x());
    p.y() = p1.y() + mu * (p2.y() - p1.y());
    p.z() = p1.z() + mu * (p2.z() - p1.z());

    return(p);
}



template <typename T1, typename T2>
static int Polygonise(GRIDCELL<T1,T2> grid,T2 isolevel,TRIANGLE<T1> *triangles){

   int i,ntriang;
   int cubeindex;
   geo::Point3<T1> vertlist[12];

   /*
      Determine the index into the edge table which
      tells us which vertices are inside of the surface
   */
   cubeindex = 0;

   if (grid.val[0] < isolevel) cubeindex |= 1;
   if (grid.val[1] < isolevel) cubeindex |= 2;
   if (grid.val[2] < isolevel) cubeindex |= 4;
   if (grid.val[3] < isolevel) cubeindex |= 8;
   if (grid.val[4] < isolevel) cubeindex |= 16;
   if (grid.val[5] < isolevel) cubeindex |= 32;
   if (grid.val[6] < isolevel) cubeindex |= 64;
   if (grid.val[7] < isolevel) cubeindex |= 128;

//   std::cout << "cube index " << cubeindex << "\n";

   /* Cube is entirely in/out of the surface */
   if (edgeTable[cubeindex] == 0)
      return(0);

   /* Find the vertices where the surface intersects the cube */
   if (edgeTable[cubeindex] & 1)
      vertlist[0] =
         VertexInterp(isolevel,grid.p[0],grid.p[1],grid.val[0],grid.val[1]);
   if (edgeTable[cubeindex] & 2)
      vertlist[1] =
         VertexInterp(isolevel,grid.p[1],grid.p[2],grid.val[1],grid.val[2]);
   if (edgeTable[cubeindex] & 4)
      vertlist[2] =
         VertexInterp(isolevel,grid.p[2],grid.p[3],grid.val[2],grid.val[3]);
   if (edgeTable[cubeindex] & 8)
      vertlist[3] =
         VertexInterp(isolevel,grid.p[3],grid.p[0],grid.val[3],grid.val[0]);
   if (edgeTable[cubeindex] & 16)
      vertlist[4] =
         VertexInterp(isolevel,grid.p[4],grid.p[5],grid.val[4],grid.val[5]);
   if (edgeTable[cubeindex] & 32)
      vertlist[5] =
         VertexInterp(isolevel,grid.p[5],grid.p[6],grid.val[5],grid.val[6]);
   if (edgeTable[cubeindex] & 64)
      vertlist[6] =
         VertexInterp(isolevel,grid.p[6],grid.p[7],grid.val[6],grid.val[7]);
   if (edgeTable[cubeindex] & 128)
      vertlist[7] =
         VertexInterp(isolevel,grid.p[7],grid.p[4],grid.val[7],grid.val[4]);
   if (edgeTable[cubeindex] & 256)
      vertlist[8] =
         VertexInterp(isolevel,grid.p[0],grid.p[4],grid.val[0],grid.val[4]);
   if (edgeTable[cubeindex] & 512)
      vertlist[9] =
         VertexInterp(isolevel,grid.p[1],grid.p[5],grid.val[1],grid.val[5]);
   if (edgeTable[cubeindex] & 1024)
      vertlist[10] =
         VertexInterp(isolevel,grid.p[2],grid.p[6],grid.val[2],grid.val[6]);
   if (edgeTable[cubeindex] & 2048)
      vertlist[11] =
         VertexInterp(isolevel,grid.p[3],grid.p[7],grid.val[3],grid.val[7]);

   /* Create the triangle */
   ntriang = 0;
   for (i=0;triTable[cubeindex][i]!=-1;i+=3) {
      triangles[ntriang].p[0] = vertlist[triTable[cubeindex][i  ]];
      triangles[ntriang].p[1] = vertlist[triTable[cubeindex][i+1]];
      triangles[ntriang].p[2] = vertlist[triTable[cubeindex][i+2]];
      ntriang++;
   }

   return(ntriang);
}


static size_t count_neighbours(std_v3<geo::VoxelOctreeNode<float>*> &nodes, geo::Pt3<int> id, size_t side){
    int ii = id.x();
    int jj = id.y();
    int kk = id.z();

    size_t count = 0;
    for(int ni = ii-1; ni < ii+2; ++ni){
        for(int nj = jj-1; nj < jj+2; ++nj){
            for(int nk = kk-1; nk < kk+2; ++nk){
                if(ni == ii && nj == jj && nk == kk){
                    continue;
                }

                if(ni > 0 && nj > 0 && nk > 0 && ni < side-1 && nj < side-1 && nk < side-1){
                    if(nodes[ni][nj][nk]){
                        ++count;
                    }
                }
            }
        }
    }

    return count;
}

static std_v1<TRIANGLE<float>> marching_cube(geo::VoxelOctreeNode<float> *node, size_t depth, float iso){

    const size_t side = static_cast<size_t>(math::ipow(2, depth));

    // sort nodes
    auto sortedNodes = node->sorted_nodes<float>(depth);




    // do the marching cube
    std_v1<TRIANGLE<float>> totalTri;
    for(size_t ii = 0; ii < side; ++ii){
        for(size_t jj = 0; jj < side; ++jj){
            for(size_t kk = 0; kk < side; ++kk){
                if(sortedNodes[ii][jj][kk]){
                    std_v1<Pt3<float>> pts;

                    Pt3<int> id(ii,jj,kk);

                    const auto &b = sortedNodes[ii][jj][kk]->bounds;
                    GRIDCELL<float,float> grid;

                    for(int ii = 0; ii < 8; ++ii){
                        grid.p[ii]   = b.origin + mu<float>[ii]*b.size*2.f;
                        grid.val[ii] = count_neighbours(sortedNodes, id + Pt3<int>(mu<float>[ii].x(), mu<float>[ii].y(), mu<float>[ii].z()), side)/26.f;
                    }

//                    grid.p[1] = b.origin + mu<float>[1]*b.size*2.f;
//                    grid.p[2] = b.origin + mu<float>[2]*b.size*2.f;
//                    grid.p[3] = b.origin + mu<float>[3]*b.size*2.f;
//                    grid.p[4] = b.origin + mu<float>[4]*b.size*2.f;
//                    grid.p[5] = b.origin + mu<float>[5]*b.size*2.f;
//                    grid.p[6] = b.origin + mu<float>[6]*b.size*2.f;
//                    grid.p[7] = b.origin + mu<float>[7]*b.size*2.f;

                    std::array<TRIANGLE<float>,5> tri;
                    int nbTri = Polygonise(grid, iso, tri.data());
                    if(nbTri > 0){
//                        std::cout << "tri " << nbTri << "\n";
                        std::move(tri.begin(), tri.begin() + nbTri, std::back_inserter(totalTri));
                    }

                }
            }
        }
    }

    return totalTri;
}


